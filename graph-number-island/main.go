package main

import "fmt"

func dfs(visited map[string]bool, grid [][]byte, rowIndex, colIndex int) {
	if rowIndex < 0 || colIndex < 0 || rowIndex >= len(grid) || colIndex >= len(grid[0]) {
		return
	}

	row := grid[rowIndex]
	col := row[colIndex]

	if col != '1' {
		return
	}
	if visited[fmt.Sprintf("%d,%d", rowIndex, colIndex)] {
		return
	}

	visited[fmt.Sprintf("%d,%d", rowIndex, colIndex)] = true

	// down
	dfs(visited, grid, rowIndex+1, colIndex)
	// up
	dfs(visited, grid, rowIndex-1, colIndex)
	// right
	dfs(visited, grid, rowIndex, colIndex+1)
	// left
	dfs(visited, grid, rowIndex, colIndex-1)

}

func numIslands(grid [][]byte) int {

	visited := make(map[string]bool)
	result := 0
	for i := 0; i < len(grid); i++ {
		for j := 0; j < len(grid[i]); j++ {
			if grid[i][j] == '1' && !visited[fmt.Sprintf("%d,%d", i, j)] {
				dfs(visited, grid, i, j)
				result++
			}
		}
	}
	return result
}

